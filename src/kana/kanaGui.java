    private void btnStartClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnStartClicked
        sound("click");
        
        lbl.setVisible(true);

        getSelectedSymbols();
        if(numOfSelectedSym == 0)
        lbl.setText("Opps! No symbol selected!");
        else{
            lblScore.setVisible(true);
            btnStart.setVisible(false);
            txtAnswer.setVisible(true);
            btnSubmit.setVisible(true);
            btnQuit.setVisible(true);

            // hide other buttons during test
            btnSymbols.setVisible(false);
            btnTutorial.setVisible(false);
            btnTest.setVisible(false);
            btnSettings.setVisible(false);
            btnAbout.setVisible(false);

            // set all to default
            counter = 1;
            score = 0;
            wrongAns = 0;
            
            lbl.setText(counter + " / " + selectedRange);
            lblScore.setText("Score: " + score);
            nextSymbol();
        }
    }//GEN-LAST:event_btnStartClicked

