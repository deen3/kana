    private void checkAnswer(){
        // if range is reached, then end of test
        if (counter == selectedRange){
            if(txtAnswer.getText().equals(currentSymbol))
                score++;
            else
                wrongAns++;
            
            lbl.setText("Test Completed!");
            lblScore.setText("Score: " + score + " / " + selectedRange);
            
            panelNotVisible();
            panelStatistics.setVisible(true);
            /////////////////////////////////////////////////////////
            // statistics data
            ////////////////////////////////////////////////////////
            DefaultListModel<String> model = new DefaultListModel<>();
            //JList<String> list;
            jListTested = new JList<>(model);
            HashMap<String, String> data = new HashMap<>();
    
            lblCorrect.setText("" + score);
            lblWrong.setText("" + wrongAns);
           
            for(int i=0; i<26; i++){
                for(int j=0; j<5; j++){
                    data.put(symbolsArray[selectedSymbols[i]][j], "/img/symbols/Hiragana/" + symbolsArray[selectedSymbols[i]][j] + ".jpg");
                }
            }
            
            for(Object x: data.keySet()){
                model.addElement(String.valueOf(x));
            }
            
            jScrollPane3 = new JScrollPane(jListTested);
            //  end -- statistics data

        }else{
            if(txtAnswer.getText().equals(currentSymbol)){
                score++;  
            }else{
                wrongAns++;
            }
            
            counter++;  // iterates counter until limit

            lbl.setText(counter + " / " + selectedRange);
            lblScore.setText("Score: " + score);
            nextSymbol();
            
            txtAnswer.setText("");
        }
    }
    
