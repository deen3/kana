    private void nextSymbol(){
        String routeSymbol = null;
        
        Random generator = new Random();
        int randNumber;
        int randIndex;  // where the first random index of the 2D array is placed
        
        // gets the randomized first index of the 2D array 
        randNumber = Math.abs(generator.nextInt()) % numOfSelectedSym;
        randIndex = selectedSymbols[randNumber];
        
        randNumber = Math.abs(generator.nextInt()) % 5;
        
        currentSymbol = symbolsArray[randIndex][randNumber];
        
        if(rbPracHir.isSelected() == true)
            routeSymbol = "/img/symbols/Hiragana/" + symbolsArray[randIndex][randNumber] + ".jpg";
        else if(rbPracKat.isSelected() == true)
            routeSymbol = "/img/symbols/Katakana/" + symbolsArray[randIndex][randNumber] + ".jpg";
           
        lblSymbols.setIcon(new javax.swing.ImageIcon(getClass().getResource(routeSymbol)));
        //System.out.println("Opps! Theres an error here!");
    }
    
    
